# Faça um programa que leia número inteiro qualquer e mostre na tela a sua tabuada.
n = int(input('Digite um número inteiro e tenha acessado a sua tabuada de 1 ao 3:'))
n1 = n*1
n2 = n*2
n3 = n*3
print ('A tabuada de 1 ao 3 do número {0} equivale a:\n {0}x1 = {1}\n {0}x2 = {2}\n {0}x3 = {3}'.format(n,n1,n2,n3))